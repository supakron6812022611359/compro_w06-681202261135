//3.คำนวณค่า j=7/2 ดังนั้น j=3
//4.คำนวณค่า k=3%2 ดังนั้น k=1
//5.คำนวณค่า i=(3+1)*3 ดังนั้น i=12
            //ผลลัพธ์สุดท้ายคือ i=12 j=3 k=1

//2.3คำนวณค่า y=(7.0*3.0)+4.0 ดังนั้น y=25.0
//2.4คำนวณค่า x=-0.5*25.0 ดังนั้น x=-12.5
//2.5คำนวณค่า z=-12.5+25.0 ดังนั้น z=12.5
            //ผลลัพธ์สุดท้ายคือ x=-12.5 y=25.0 z=12.5